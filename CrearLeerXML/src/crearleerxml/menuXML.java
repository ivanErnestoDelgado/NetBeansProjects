    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        textApellidos.setText("");
        textNControl.setText("");
        textCarrera.setText("");
        textNombres.setText("");
        
        prepararCambio();
        
        accion=true;
    }//GEN-LAST:event_botonAgregarActionPerformed

    private void botonListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListoActionPerformed
        if(accion){
            agregar();
        }else{
            cambiar();
        }
        
        
        botonAgregar.setEnabled(true);
        botonAnterior.setEnabled(true);
        botonCambiar.setEnabled(true);
        botonEliminar.setEnabled(true);
        botonGuardar.setEnabled(true);
        botonSiguiente.setEnabled(true);

        
        textNControl.setEnabled(false);
        textApellidos.setEnabled(false);
        textCarrera.setEnabled(false);
        textNombres.setEnabled(false);
        
        botonListo.setEnabled(false);
        textTsbla.setText(metodosXML.mostrar());
    }//GEN-LAST:event_botonListoActionPerformed

    private void botonCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCambiarActionPerformed
       prepararCambio();
       
       accion=false;
       
       textTsbla.setText(metodosXML.mostrar());
    }//GEN-LAST:event_botonCambiarActionPerformed

    private void botonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSiguienteActionPerformed
        posicion++;
        if(posicion>=metodosXML.arrayAux.size()){
        posicion=0;
        }
        visualizar(posicion);
    }//GEN-LAST:event_botonSiguienteActionPerformed

    private void botonAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnteriorActionPerformed
         posicion--;
        if(posicion<0){
        posicion=(short) (metodosXML.arrayAux.size()-1);
        }
        visualizar(posicion);
    }//GEN-LAST:event_botonAnteriorActionPerformed

    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        try {
            metodosXML.guardar();
        } catch (IOException ex) {
            Logger.getLogger(menuXML.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        metodosXML.quitar(posicion);
        if(metodosXML.arrayAux.size()>1){
        posicion-=1;
        visualizar(posicion);
        }
        else{
        textApellidos.setText("");
        textNControl.setText("");
        textCarrera.setText("");
        textNombres.setText("");
        
        botonSiguiente.setEnabled(false);
        botonAnterior.setEnabled(false);
        botonEliminar.setEnabled(false);
        }
        textTsbla.setText(metodosXML.mostrar());
    }//GEN-LAST:event_botonEliminarActionPerformed

