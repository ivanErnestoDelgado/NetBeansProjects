    private void agregar() {
       Usuario alumno= new Usuario(textNControl.getText(), textNombres.getText(), textApellidos.getText(), textCarrera.getText());
        metodosXML.agregar(alumno);
        posicion=(short) (metodosXML.arrayAux.size()-1);
        botonEliminar.setEnabled(false);
    }

    private void cambiar() {
        Usuario alumno= new Usuario(textNControl.getText(), textNombres.getText(), textApellidos.getText(), textCarrera.getText());
        metodosXML.cambiar(posicion,alumno);
    }

    private void prepararCambio() {
        textNControl.setEnabled(true);
        textApellidos.setEnabled(true);
        textCarrera.setEnabled(true);
        textNombres.setEnabled(true);
        
 
        botonAgregar.setEnabled(false);
        botonAnterior.setEnabled(false);
        botonCambiar.setEnabled(false);
        botonEliminar.setEnabled(false);
        botonGuardar.setEnabled(false);
        botonSiguiente.setEnabled(false);

        
        botonListo.setEnabled(true);
    }

    private void visualizar(short pos) {
        Usuario alumno=metodosXML.visualizar(pos);
        
        textNControl.setText(alumno.getNumControl());
        textApellidos.setText(alumno.getApellidos());
        textCarrera.setText(alumno.getCarrera());
        textNombres.setText(alumno.getNombres());
   }
